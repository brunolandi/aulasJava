package repeticoes;

public class exercicioFor2 {

	public static void main(String[] args) {

		// contagem
		for (int i = 0; i <= 5; i++) {
			System.out.println("Valor de i: " + i);
		}

		System.out.println();

		// contagem regressiva
		for (int i = 5; i >= 0; i--) {
			System.out.println("Valor de i: " + i);
		}

	}

}

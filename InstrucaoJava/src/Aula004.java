
public class Aula004 {

	public static void main(String[] args) {

		int n = 5;
		
		switch(n) {
		
		case 1:
			System.out.println("Voce digitou 1");
			break;
		case 2:
			System.out.println("Voce digitou 2");	
			break;
		case 3:
			System.out.println("Voce digitou 3");
			break;
		default:
			System.out.println("Número inválido");
		
		}

	}

}
